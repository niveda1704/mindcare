import i18n from 'i18next';
import { initReactI18next } from 'react-i18next';
import LanguageDetector from 'i18next-browser-languagedetector';
import Backend from 'i18next-http-backend'; // For loading translations via http (if we used public folder)
// but since I put them in src/locales, I should probably import them directly to avoid build steps unless I configure vite to copy them.
// Importing directly is easier for Vite without extra config for simple apps.

import enTranslation from './locales/en/translation.json';
import hiTranslation from './locales/hi/translation.json';
import taTranslation from './locales/ta/translation.json';

i18n
    .use(LanguageDetector)
    .use(initReactI18next)
    .init({
        resources: {
            en: {
                translation: enTranslation
            },
            hi: {
                translation: hiTranslation
            },
            ta: {
                translation: taTranslation
            }
        },
        lng: "en", // default language
        fallbackLng: "en",
        interpolation: {
            escapeValue: false // react already safes from xss
        }
    });

export default i18n;
